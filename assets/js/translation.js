$(document).ready(function(e) {
    
    function translatePage(language) {
        $('[data-translate-key]').each(function() {
            const key = $(this).data('translate-key');
            $(this).text(translations[language][key]);
        });

        $('.lang-btn').removeClass('active_lang');
        $(`.lang-btn[data-lang="${language}"]`).addClass('active_lang');
    }

    // Set default language (e.g., English)
    translatePage('am');

    // Change language on button click
    $('.dropdown_btn').on('click', function(e) {
        if($('.dropdown_btn').hasClass('show')){
            $('.dropdown_btn img').css('transform', 'rotate(180deg)');
        }else{
            $('.dropdown_btn img').css('transform', 'none');
        }
    });
    $('.lang-btn').on('click', function(e) {
        e.preventDefault();
        const selectedLang = $(this).data('lang');
        const selectedText = $(this).text();
        $('.dropdown_btn img').css('transform', 'none');
        $('.my_selected_lang').text(selectedText);
        translatePage(selectedLang);
    });
});